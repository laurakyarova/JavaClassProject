package class2;

public class E10ShortHandOperators {
    public static void main(String[] args) {

        int number=10;
        number=number+10;
        System.out.println(number);

        int number1=10;
        number1+=30;
        System.out.println(number1);

        int number2=100;
        number2-=40;
        System.out.println(number2);

        int number3=10;
        number3*=3;
        System.out.println(number3);

        int number4=12;
        number4/=3;
        System.out.println(number4);


    }
}
