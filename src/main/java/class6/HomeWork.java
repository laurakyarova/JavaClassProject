package class6;

public class HomeWork {
    public static void main(String[] args) {


        System.out.println("For Loop ");

        for (int j= 20; j>=1; j-=2) {
            System.out.print(j+" ");
        }
        System.out.println("\n While loop ");
        int i=20;
        while(i>=1){
            System.out.print(i+" ");
            i-=2;
        }
    }

}
